#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = ft_strlen((char *)s);
	while (i >= 0 && c != s[i])
		i--;
	return (s[i] == c ? (char *)(s + i) : 0);
}
